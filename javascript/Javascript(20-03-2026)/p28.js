/**
 * Question: Write a function to find the remainder of two numbers.
 * 
 * Instructions:
 * Return the remainder when 'num1' is divided by 'num2'.
 * 
 * Example:
 * num1 = 10, num2 = 3 -> the result should be 1
 * Reason: When 10 is divided by 3, the remainder is 1.
 */

function remainder(num1, num2) {
  
  return num1 % num2;
}

console.log(remainder(10, 3)); 
console.log(remainder(15, 4)); 
